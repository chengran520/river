package com.gxhy.utils;

import org.springframework.boot.context.properties.ConfigurationProperties;
import org.springframework.stereotype.Component;

@Component
@ConfigurationProperties(prefix = "system-params")
public class ReadApplicationUntil{
 
	public static String imageUri;
	
	public static  String fileAddres;

	public static String getImageUri() {
		return imageUri;
	}

	public void setImageUri(String imageUri) {
		this.imageUri = imageUri;
	}

	public static String getFileAddres() {
		return fileAddres;
	}

	public void setFileAddres(String fileAddres) {
		this.fileAddres = fileAddres;
	}
 
   
}